
#include<iostream>

using namespace std;

main()
{
    cout<<"Comparision Operator "<<endl;

    int  a = 10, b = 5;

    //True = 1 and False = 0
   
    cout<< (a==b) <<endl;   //Return true when value of variable a is equal to b.
    cout<< (a!=b) <<endl;   //Return true when value of variable a is not equal to b.
    cout<< (a>b) <<endl;
    cout<< (a<b) <<endl;
    cout<< (a>=b) <<endl;
    cout<< (a<=b) <<endl;
    cout<< !(a==b) <<endl;  //(a!=b)
    
}